def get_rules_for_prompt(
    dominant_types: list[str],
    source: str = "cust",
    cause: str | None = None,
    max_rules: int = 6,
) -> str:
    """수칙 목록을 LLM 프롬프트용 문자열로 반환.

    Args:
        dominant_types: 해당 리프의 주요 사고유형 리스트 (빈도 내림차순)
        source: 'cust' 또는 'emp'
        cause: 고객 사고의 원인1 (선택)
        max_rules: 최대 수칙 수

    Returns:
        줄글 형태의 수칙 목록 문자열
    """
    rule_db = CUSTOMER_RULES if source == "cust" else EMPLOYEE_RULES
    collected: list[str] = []

    for dtype in dominant_types:
        rules = rule_db.get(dtype, [])
        for r in rules:
            if r not in collected:
                collected.append(r)
        if len(collected) >= max_rules:
            break

    # 원인1 보완 (고객 사고)
    if source == "cust" and cause and len(collected) < max_rules:
        cause_rules = CUSTOMER_CAUSE_RULES.get(cause, [])
        for r in cause_rules:
            if r not in collected:
                collected.append(r)
            if len(collected) >= max_rules:
                break

    if not collected:
        collected = ["작업 시작 전 주변 환경을 확인하고 이상 발견 시 즉시 관리자에게 보고한다."]

    lines = [f"{i+1}. {r}" for i, r in enumerate(collected[:max_rules])]
    return "\n".join(lines)
