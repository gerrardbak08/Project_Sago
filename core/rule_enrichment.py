def get_feature_thresholds(source: str | None = None) -> dict:
    """공통 룰 기준표를 반환한다. source 인자는 호출부 호환을 위해 유지한다."""
    return FEATURE_RISK_THRESHOLDS


def classify_feature_bucket(
    source: str | None,
    feature: str,
    value: Any,
) -> dict | None:
    """피처 값을 공통 기준표의 구간 라벨로 분류한다."""
    try:
        numeric = float(value)
    except (TypeError, ValueError):
        return None

    thresholds = FEATURE_RISK_THRESHOLDS.get(feature)
    if not thresholds:
        return None

    for label, info in thresholds.items():
        op = info["op"]
        threshold = float(info["val"])
        if (
            (op == "<=" and numeric <= threshold)
            or (op == "<" and numeric < threshold)
            or (op == ">" and numeric > threshold)
            or (op == ">=" and numeric >= threshold)
        ):
            return {
                "feature": feature,
                "label": label,
                "value": numeric,
                "op": op,
                "threshold": threshold,
                "risk": info["risk"],
            }

    return None
