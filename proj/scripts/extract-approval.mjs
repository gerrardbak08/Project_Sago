#!/usr/bin/env node
/**
 * 산재 승인 현황 Excel → src/data/approvalData.js
 *
 * [1차] DB/산재승인현황.xlsx 를 읽어 승인 통계를 생성.
 * [2차] HR API 엔드포인트 연동 후 이 스크립트 대신 API 호출로 교체 예정.
 *
 * Usage:
 *   cd proj && node scripts/extract-approval.mjs
 *
 * ── 컬럼 매핑 (Excel 받은 후 TODO 항목 교체) ──────────────────
 * TODO: 아래 COL_* 상수를 실제 Excel 헤더명으로 교체하세요.
 */
import * as XLSX from 'xlsx';
import { writeFileSync, readFileSync, existsSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT     = resolve(__dirname, '..', '..');
const DB       = resolve(ROOT, 'DB');
const OUT      = resolve(__dirname, '..', 'src', 'data', 'approvalData.js');

const APPROVAL_XLSX = resolve(DB, '산재승인현황.xlsx');

// ── TODO: 실제 컬럼명으로 교체 ─────────────────────────────────
const COL_YEAR      = '연도';          // 예: 2024, 2025, 2026
const COL_MONTH     = '월';            // 예: 1 ~ 12
const COL_STATUS    = '승인여부';      // 예: "승인" | "불승인" | "심사중"
const COL_DEPT      = '부서';          // 예: "경북영업부"
const COL_TYPE      = '재해유형';      // 예: "넘어짐"
// ───────────────────────────────────────────────────────────────

function readSheet(path) {
  if (!existsSync(path)) {
    console.error(`[ERROR] 파일 없음: ${path}`);
    console.error('  → DB/산재승인현황.xlsx 를 배치 후 다시 실행하세요.');
    process.exit(1);
  }
  const wb = XLSX.read(readFileSync(path), { type: 'buffer', cellDates: true });
  const sheet = wb.Sheets[wb.SheetNames[0]];
  return XLSX.utils.sheet_to_json(sheet, { defval: null });
}

function processApproval(rows) {
  const STATUS = { APPROVED: '승인', REJECTED: '불승인', PENDING: '심사중' };

  let approved = 0, rejected = 0, pending = 0;
  const byYear  = {};  // { 2024: { approved, rejected, pending }, ... }
  const byDept  = {};  // { "경북영업부": { approved, rejected, pending }, ... }
  const byType  = {};  // { "넘어짐": { approved, rejected, pending }, ... }

  for (const r of rows) {
    const status = String(r[COL_STATUS] || '').trim();
    const year   = parseInt(r[COL_YEAR]) || null;
    const dept   = String(r[COL_DEPT] || '').trim() || null;
    const type   = String(r[COL_TYPE] || '').trim() || null;

    const isApproved = status === STATUS.APPROVED;
    const isRejected = status === STATUS.REJECTED;
    const isPending  = status === STATUS.PENDING;

    if (isApproved) approved++;
    else if (isRejected) rejected++;
    else if (isPending) pending++;

    if (year) {
      if (!byYear[year]) byYear[year] = { approved: 0, rejected: 0, pending: 0 };
      if (isApproved) byYear[year].approved++;
      else if (isRejected) byYear[year].rejected++;
      else if (isPending) byYear[year].pending++;
    }

    if (dept) {
      if (!byDept[dept]) byDept[dept] = { approved: 0, rejected: 0, pending: 0 };
      if (isApproved) byDept[dept].approved++;
      else if (isRejected) byDept[dept].rejected++;
      else if (isPending) byDept[dept].pending++;
    }

    if (type) {
      if (!byType[type]) byType[type] = { approved: 0, rejected: 0, pending: 0 };
      if (isApproved) byType[type].approved++;
      else if (isRejected) byType[type].rejected++;
      else if (isPending) byType[type].pending++;
    }
  }

  const total = approved + rejected + pending;
  const approvalRate = total > 0 ? parseFloat((approved / total * 100).toFixed(1)) : null;

  return { total, approved, rejected, pending, approvalRate, byYear, byDept, byType };
}

const rows = readSheet(APPROVAL_XLSX);
const data = processApproval(rows);

const output = `// === Auto-generated by proj/scripts/extract-approval.mjs ===
// 1차: DB/산재승인현황.xlsx  |  2차: HR API 자동 연동 예정
// processed/*.csv 는 절대 수정 금지.

const APPROVAL_DATA = ${JSON.stringify(data, null, 2)};
export default APPROVAL_DATA;
`;

writeFileSync(OUT, output, 'utf8');
console.log(`✓ approvalData.js 생성 완료 (총 ${data.total}건 / 승인 ${data.approved}건 / 불승인 ${data.rejected}건 / 심사중 ${data.pending}건)`);
